class UTC(tzinfo):
    """UTC"""

    zero_offset = timedelta(0)

    def utcoffset(self, dt):
        return self.zero_offset

    def tzname(self, dt):
        return "UTC"

    def dst(self, dt):
        return self.zero_offset


