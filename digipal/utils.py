            KeyVal.set('k2.b', reindexes_str)
            try:
                call_command('dpsearch', 'index_facets',
                             index_filter=reindexes_str)
            except Exception as e:
                KeyVal.set('k2.c', 'ERROR: %s' % repr(e))
